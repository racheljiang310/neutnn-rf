def encode_single_window(x_flat, coder, early_cutoff=None):
    """Encode a single flattened window using the appropriate method for the encoder type.

    - PopulationCoder: returns spike times with Inf for no-spike → use isfinite()
    - DeltaSpikeEncoder (and similar): returns binary vectors directly → use as-is
    """
    x_t = torch.from_numpy(np.asarray(x_flat, dtype=np.float32))

    if hasattr(coder, 'encoder') and coder.encoder in ('delta', 'mr_delta', 'phase'):
        # Delta/Phase encoders return binary vectors directly
        return coder.encode(x_t).float()
    elif hasattr(coder, 'encoder') and coder.encoder in ("SQLE"):
      spike_times = coder.encode(x_t)
      if early_cutoff is None:
          active = torch.isfinite(spike_times)
      else:
          active = torch.isfinite(spike_times) & (spike_times <= early_cutoff)
      return active.float().reshape(-1)

    # Default fallback: assume spike-time encoder
    else:
        spike_times = coder.encode(x_t)
        if early_cutoff is None:
            active = torch.isfinite(spike_times)
        else:
            active = torch.isfinite(spike_times) & (spike_times <= early_cutoff)
        return active.float().reshape(-1)

@torch.no_grad()
def predict_rnl_classes_from_windows(W, coder, rnl_model, early_cutoff=None):
    """
    W: (N, 2, window_len)
    returns: pred4 of shape (N,)
    """
    preds = []

    for i in range(W.shape[0]):
        x_flat = W[i].reshape(-1).astype(np.float32)
        x_bin = encode_single_window(x_flat, coder, early_cutoff=early_cutoff)
        data_in = make_model_input(x_bin, num_neurons=rnl_model.numNeurons, device=device)

        _inputData, _outputData = rnl_model(data_in)
        pred, _times = predict_class_from_wta(rnl_model)
        preds.append(pred)

    return np.asarray(preds, dtype=np.int64)

def user_def_demod_fn(sig_mixture, interference_sig_type=None):
    """
    Demodulate one challenge waveform using the trained 4-class RNL model.

    Input:
      sig_mixture: (sig_len,) complex64
      interference_sig_type: unused here, included for API compatibility

    Returns:
      bit_est: (sig_len,) int64
    """
    # Use the global window_len variable (set in Hyperparameters cell).
    # This works for any encoder (Gaussian, Delta, Phase, etc.)
    wl = window_len

    # 1) complex -> X (2,40960)
    X = complex_to_X(sig_mixture)

    # 2) slice into symbol-centered windows W (2560,2,window_len)
    W, _ = slice_symbol_windows_X(X, sps=16, tau0=8, window_len=wl, n_sym=2560)

    # 3) run trained RNL classifier on each window
    pred4 = predict_rnl_classes_from_windows(
        W,
        coder=coder,
        rnl_model=rnl_model,
        early_cutoff=early_cutoff if 'early_cutoff' in globals() else None
    )  # (2560,)

    # 4) class4 -> bits
    bit_est = class4_to_bits(pred4)  # (5120,)

    return bit_est.astype(np.int64)

