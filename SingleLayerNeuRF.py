def make_input_labels(label, num_neurons, num_dendrites, num_segments, numDistalInputs, device):
    """
    Build inputLabels in MNIST-like active-dendrite format.

    Shape before reshape:
      [num_neurons, num_dendrites, num_segments, numDistalInputs]

    Interpretation:
      - dendrite index = class index
      - all segments of the correct dendrite are marked as 1
      - all other dendrites are 0
    """
    labels = torch.zeros(
        num_neurons,
        num_dendrites,
        num_segments,
        numDistalInputs,
        device=device
    )
    labels[:, int(label), :, :] = 1.0
    return labels.reshape(num_neurons * num_dendrites * num_segments, numDistalInputs)


def predict_class_from_wta(rnl_model):
    """
    MNIST-like one-layer prediction:
      - interpret dendrites as class buckets
      - take earliest segment time per dendrite
      - predict the class with earliest dendrite spike

    Works best when:
      numDendrites = NUM_CLASSES
      numNeurons   = 1
    """
    wta = rnl_model.wtaOutput.view(
        rnl_model.numNeurons,
        rnl_model.numDendrites,
        rnl_model.numSegments
    )

    # Since we are using numNeurons = 1, this becomes [numDendrites, numSegments]
    dendrite_times = wta.min(dim=2)[0].squeeze(0)

    pred = torch.argmin(dendrite_times).item()
    return pred, dendrite_times.detach().cpu()

