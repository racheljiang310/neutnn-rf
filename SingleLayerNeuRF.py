"""### Conversion Helpers"""

def class4_to_bits(pred4):
    """
    class4_to_bits: converts classification id to respsective bit value

    Args: pred4: Tensor(n_sym,) integer classes in {0,1,2,3}
    returns: Tensor(5120,) int64 bits
    """
    pred4 = np.asarray(pred4, dtype=np.int64).reshape(-1)
    bits = np.zeros(2 * len(pred4), dtype=np.int64)
    for k, c in enumerate(pred4):
        b0, b1 = _class_to_pair[int(c)]
        bits[2*k]   = b0
        bits[2*k+1] = b1
    return bits

def complex_to_X(sig_complex):
    """
    complex_to_X: expands array of raw complex numbers into array of coefficient pairs

    Args:
      sig_complex: (sig_len,) complex

    returns: (2, sig_len) float32
    """
    sig_complex = np.asarray(sig_complex)
    return np.vstack([sig_complex.real, sig_complex.imag]).astype(np.float32)

def bits_to_symbol_classes(bits):
    b = np.asarray(bits, dtype=np.int64).reshape(-1)
    pairs = b.reshape(-1, 2)  # (2560,2)
    y = np.array([_pair_to_class[tuple(p)] for p in pairs], dtype=np.int64)  # (2560,)
    return y

def slice_symbol_windows_X(X, sps=16, tau0=8, window_len=127, n_sym=2560):
    """
    X: (2, N) float32
    returns:
      W: (n_sym, 2, window_len)
      centers: (n_sym,)
    """
    assert X.ndim == 2 and X.shape[0] == 2
    N = X.shape[1]
    half_left = window_len // 2
    half_right = window_len - half_left - 1

    centers = tau0 + sps * np.arange(n_sym, dtype=np.int64)
    W = np.zeros((n_sym, 2, window_len), dtype=np.float32)

    for k, c in enumerate(centers):
        left = int(c) - half_left
        right = int(c) + half_right  # inclusive
        src_l = max(left, 0)
        src_r = min(right, N-1)
        dst_l = src_l - left
        dst_r = dst_l + (src_r - src_l)
        W[k, :, dst_l:dst_r+1] = X[:, src_l:src_r+1]

    return W, centers

