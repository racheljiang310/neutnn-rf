class TBRCoder:
    """Threshold-Based Representation (TBR) encoder.

    Generates spikes from first-order temporal differences that exceed an
    adaptive per-sample threshold.  Two channels are produced per timestep:

      ON  channel (col 0): spike (time=0) when  delta[t] > +threshold
      OFF channel (col 1): spike (time=0) when  delta[t] < -threshold

    Adaptive threshold = threshold_factor * std(delta) computed fresh for
    every individual sample, so it adjusts to each signal's own dynamics.

    Attributes:
        tmax             (int):   kept for API compatibility with PopulationCoder
        n                (int):   2  (ON + OFF channels)
        threshold_factor (float): scales std(delta) to set the threshold
        scaler           (obj):   StandardScaler fitted on training data
    """

    def __init__(self, data, tmax, threshold_factor=1.0):
        """
        Args:
            data             : (num_samples, data_len) array — training set
            tmax             : temporal window size (kept for compatibility)
            threshold_factor : multiplier on per-sample std(delta)
        """
        self.encoder = 'tbr'
        self.tmax = tmax
        self.n = 2                          # ON + OFF channels
        self.threshold_factor = threshold_factor

        self.data_len = data.shape[1]
        self.scaler = StandardScaler()
        self.scaler.fit(data)

    def encode(self, tensor):
        """
        Args:
            tensor: raw 1-D signal of shape (data_len,)

        Returns:
            spike_times: shape (data_len, 2)
                         0.0   = spike fired at time 0
                         Inf   = no spike
        """
        # Scale to zero mean / unit variance (uses training statistics)
        # x = self.scaler.transform(tensor.unsqueeze(0) if isinstance(tensor, torch.Tensor)
        #                           else torch.from_numpy(tensor).unsqueeze(0)).squeeze()
        # x = torch.from_numpy(x) if not isinstance(x, torch.Tensor) else x

        x = tensor if isinstance(tensor, torch.Tensor) else torch.from_numpy(tensor)
        x = x.float()

        # First-order temporal differences; pad t=0 with 0
        delta = torch.zeros_like(x)
        delta[1:] = x[1:] - x[:-1]

        # # Adaptive threshold: per-sample std of deltas
        # threshold = self.threshold_factor * delta.std()
        threshold = torch.mean(delta) + self.threshold_factor * delta.std()
        # # Now using mad
        # mad = torch.median(torch.abs(delta - torch.median(delta)))
        # threshold = self.threshold_factor * mad * 1.4826

        inf = float('inf')
        on_spikes  = torch.where(delta >  threshold,
                                 torch.zeros_like(delta),
                                 torch.full_like(delta, inf))
        off_spikes = torch.where(delta < -threshold,
                                 torch.zeros_like(delta),
                                 torch.full_like(delta, inf))

        return torch.stack([on_spikes, off_spikes], dim=-1)   # (data_len, 2)

