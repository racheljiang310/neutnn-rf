def make_single_neuron_input(x_vec):
    """
    x_vec: shape (D,)
    returns shape (1, D) because we use numNeurons=1 in both layers
    """
    if not torch.is_tensor(x_vec):
        x_vec = torch.tensor(x_vec, dtype=torch.float32, device=device)
    else:
        x_vec = x_vec.to(device).float()
    return x_vec.unsqueeze(0)

def layer_dendrite_times_rnl(layer):
    """
    For activeDendriteLayer_RNL after a forward pass:
      layer.wtaOutput shape = [numNeurons*numDendrites, numSegments]
    Since we use numNeurons=1, this becomes [numDendrites, numSegments].

    Returns earliest segment time per dendrite, shape [numDendrites].
    Smaller = earlier spike. wMax = no spike.
    """
    wta = layer.wtaOutput.view(layer.numNeurons, layer.numDendrites, layer.numSegments)
    dend_times = wta.min(dim=2)[0].squeeze(0)   # [numDendrites]
    return dend_times


def hidden_vector_from_layer1(layer):
    """
    Convert hidden-layer dendrite responses into a binary hidden feature vector.

    One hidden feature per dendrite:
      1 if that dendrite spiked on any segment
      0 otherwise
    """
    dend_times = layer_dendrite_times_rnl(layer)
    hidden_vec = (dend_times < layer.wMax).float()
    return hidden_vec  # [numDendrites]


def predict_class_from_layer2(layer):
    """
    Output layer has numDendrites = 4 classes.
    Predict by earliest dendrite spike time.
    If all dendrites are silent, return pred=0 and no_spike=True.
    """
    dend_times = layer_dendrite_times_rnl(layer)   # [4]
    no_spike = bool(torch.all(dend_times == layer.wMax).item())
    pred = int(torch.argmin(dend_times).item())
    return pred, dend_times, no_spike


def make_unsupervised_labels_for_layer(layer):
    """
    Unsupervised branch of update() is triggered by rewardEn=False.
    inputLabels is ignored logically in that branch, but we pass all-ones
    with the correct shape for consistency.
    """
    return torch.ones(
        layer.totalSegments,
        layer.numDistalInputs,
        device=device
    )


def make_supervised_labels_for_layer(layer, target_class):
    """
    Build inputLabels tensor of shape [totalSegments, numDistalInputs]
    in the same spirit as the one-layer activeDendrite MNIST code:
    target class activates the corresponding dendrite across all segments
    and all distal-input positions.
    """
    labels = torch.zeros(
        layer.numNeurons,
        layer.numDendrites,
        layer.numSegments,
        layer.numDistalInputs,
        device=device
    )
    labels[:, int(target_class), :, :] = 1.0
    return labels.reshape(layer.totalSegments, layer.numDistalInputs)

