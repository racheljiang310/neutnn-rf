@torch.no_grad()
def test_epoch_2layer_rnl(
    X_enc, y,
    layer1, layer2,
    max_samples=None,
    verbose_every=500
):
    n = len(X_enc) if max_samples is None else min(len(X_enc), max_samples)

    conf = torch.zeros((OUTPUT_DENDRITES_2L, OUTPUT_DENDRITES_2L), dtype=torch.int64)  # [pred, true]
    correct = 0
    total = 0
    no_spike_count = 0

    for idx in range(n):
        x = X_enc[idx].to(device).float()
        target = int(y[idx])

        # ----- Layer 1 -----
        x1_in = make_single_neuron_input(x)
        _, _ = layer1(x1_in)
        hidden_vec = hidden_vector_from_layer1(layer1)

        # ----- Layer 2 -----
        x2_in = make_single_neuron_input(hidden_vec)
        _, _ = layer2(x2_in)

        pred_class, dend_times2, no_spike2 = predict_class_from_layer2(layer2)
        if no_spike2:
            no_spike_count += 1

        conf[pred_class, target] += 1

        total += 1
        if pred_class == target:
            correct += 1

        if verbose_every is not None and (idx + 1) % verbose_every == 0:
            print(f"[test 2L] sample {idx+1}/{n}  acc={correct/total:.4f}  no_spike={no_spike_count/total:.4f}")

    class_totals = conf.sum(dim=0)
    class_correct = conf.diag()
    class_acc = class_correct.float() / class_totals.clamp(min=1).float()

    print("\nConfusion Matrix (rows=pred, cols=true):")
    print(conf)
    print("\nClass totals:", class_totals)
    print("Class correct:", class_correct)
    print("Class acc:", class_acc)
    print(f"No-spike outputs: {no_spike_count}/{total} = {no_spike_count/total:.4f}")

    return {
        "acc": correct / total,
        "confusion_matrix": conf,
        "class_totals": class_totals,
        "class_correct": class_correct,
        "class_acc": class_acc,
        "no_spike_count": no_spike_count,
        "n": total,
    }

