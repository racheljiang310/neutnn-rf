class ZeroCrossStepForwardEncoder:

    """
    A variant of the Step-Forward (SF) encoding technique that utilizes zero-crossings of the signal. Unlike the standard SF method,
    which involves a baseline (Base) value, ZCSF relies on a half-wave rectification behavior and emits spikes only for
    positive signal values exceeding a specified threshold.

    Implementation Details found: https://spikify.readthedocs.io/en/latest/spikify/encoding/temporal/contrast/zero_cross_step_forward.html

      Implementation Steps:
        1. Rectify the signal: Use `maximum` function to zero out all negative signal values.
        2. Apply t condition: Generate spikes by checking if the rectified signal exceeds the given threshold.
        3. Construct the spike train: Create an output array that holds the spike values, where spikes occur based on the defined conditions.

    Attributes:
        data        (tensor): # (train_sample_count, 2*window_len)
        theta       (float | list[float]): The threshold value used to determine spike generation. Can be a float or a list of floats.
        tolerance   (int): how close to 0 can we count as crossing
    """

    def __init__(self, theta: list[float, float], tolerance: int=0):

        """ Initializes the encoder: setting up gaussian curves, and

        Args:
          theta (list):    threshold for rectified signal => threshold should just be our theta value
          tolerance (int):  how close to 0 can we count as crossing -> we aren't using this yet tho

        """

        self.encoder = 'zcsf'
        self.theta = theta
        self.n = 1
        self.tolerance = tolerance

    def encode(self, tensor: torch.Tensor):

        """ Maps data tensor to spike train.

        Args:
            (tensor): The raw time series signal of shape (window_len*2, ) where window_len is the
              # of timesteps in one signal, 2 is for I/Q values

        Returns:
            (tensor): The spikes where float('Inf') indicates not spike.
            Returns binary spikes of shape (window_len * 2,)

        """

        # Rectify the signal
        half_rectified = torch.clamp(tensor, min=0.0)

        # 2. Spike Generation: Emit 1 when rectified signal exceeds threshold.
        real = half_rectified[0::2]
        imag = half_rectified[1::2]

        # 3. Generate boolean masks for thresholding
        mask_real = real > self.theta[0]
        mask_imag = imag > self.theta[1]

        # 4. Initialize with Infinity (never spikes)
        i_spikes = torch.full_like(real, float('Inf'))
        q_spikes = torch.full_like(imag, float('Inf'))

        # 5. Assign spike times (e.g., 1.0) only where the mask is True
        i_spikes[mask_real] = 1.0
        q_spikes[mask_imag] = 1.0

        # 3. ZCSF Logic: Thresholding & Recombine back into a single 254-length flat tensor
        spiketimes = torch.stack((i_spikes, q_spikes), dim=1).view(-1)
        return spiketimes

