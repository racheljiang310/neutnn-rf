def train_epoch_2layer_rnl(
    X_enc, y,
    layer1, layer2,
    max_samples=None,
    verbose_every=500,
    shuffle=True
):
    n = len(X_enc) if max_samples is None else min(len(X_enc), max_samples)

    if shuffle:
        order = torch.randperm(len(X_enc))[:n].tolist()
    else:
        order = list(range(n))

    correct = 0
    total = 0
    no_spike_count = 0

    for step, idx in enumerate(order):
        x = X_enc[idx].to(device).float()
        target = int(y[idx])

        # ----- Layer 1 forward -----
        x1_in = make_single_neuron_input(x)
        inputData1, outputData1 = layer1(x1_in)
        hidden_vec = hidden_vector_from_layer1(layer1)

        # ----- Layer 1 unsupervised update -----
        labels1 = make_unsupervised_labels_for_layer(layer1)
        layer1.weights.data = layer1.update(
            inputData=inputData1,
            inputLabels=labels1,
            outputData=outputData1,
            weights=layer1.weights.data,
            capture=CAPTURE_H1_2L,
            search=SEARCH_H1_2L,
            backoff=BACKOFF_H1_2L,
            wBase=WBASE_H1_2L,
            rewardEn=False,
            frac=frac
        )

        # ----- Layer 2 forward -----
        x2_in = make_single_neuron_input(hidden_vec)
        inputData2, outputData2 = layer2(x2_in)

        pred_class, dend_times2, no_spike2 = predict_class_from_layer2(layer2)
        if no_spike2:
            no_spike_count += 1

        total += 1
        if pred_class == target:
            correct += 1

        # ----- Layer 2 supervised update -----
        labels2 = make_supervised_labels_for_layer(layer2, target)
        layer2.weights.data = layer2.update(
            inputData=inputData2,
            inputLabels=labels2,
            outputData=outputData2,
            weights=layer2.weights.data,
            capture=CAPTURE_H2_2L,
            search=SEARCH_H2_2L,
            backoff=BACKOFF_H2_2L,
            wBase=WBASE_H2_2L,
            rewardEn=rewardEn,
            frac=frac
        )

        if verbose_every is not None and (step + 1) % verbose_every == 0:
            print(f"[train 2L] sample {step+1}/{n}  acc={correct/total:.4f}  no_spike={no_spike_count/total:.4f}")

    return {
        "acc": correct / total,
        "no_spike_count": no_spike_count,
        "n": total,
    }

