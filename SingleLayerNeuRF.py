def fit_minmax_scaler(X_train, eps=1e-6):
    x_min = X_train.min(axis=0, keepdims=True)
    x_max = X_train.max(axis=0, keepdims=True)
    x_range = np.clip(x_max - x_min, eps, None)
    return x_min.astype(np.float32), x_max.astype(np.float32), x_range.astype(np.float32)

# use multiplier so we don't have to make threshold super low
def apply_minmax_scaler(X, x_min, x_range, multiplier = 5, clip=True):
    X_scaled = 2.0 * ((X - x_min) / x_range) - 1.0
    if clip:
        X_scaled = np.clip(X_scaled, -1.0, 1.0)
    return (multiplier * X_scaled).astype(np.float32)

"""### Population Encoder (NCAL Legacy)
This class comes from NCAL's UCR Time-series dataset exploration paper. It will encode time-series data into spikes by doing a Gaussain reduction on the inputs, and we then convert them into sparse spikes.

"""

class PopulationCoder:

    """Gaussian receptive field population encoder.

    Takes time series sample:   [1.2, 5.3, 2.2, 6.2, ... 0.5]
    Returns its encoded format: [ [0, 5, Inf, Inf, ... 3],
                                  [1, Inf, Inf, Inf, ... 4],
                                  ....
                                  [Inf, Inf, Inf, 1, ... Inf]]

    Attributes:
        tmax    (int): Exclusive upper bound on maximum spike time
        n       (int): Number of Gaussian curves to use for each time series sample
        data    (tensor): The data that is to be encoded
        centers (tensor): Tensor of the means of each Gaussian curve
        widths  (tensor): Tensor of the widths of each Gaussian curve
        beta    (int): Common scaling factor on the width of each Gaussian curve
        scaler  (object): Used to scale incoming signals to zero mean, unit variance
    """

    def __init__(self, data, n, tmax, beta=1):

        """ Initializes the encoder: setting up gaussian curves, and

        Args:
          (data): Your raw time-series data (floats). It can be a PyTorch Tensor or a NumPy array of shape (num_samples, data_len)
            num_samples = the number of time-series examples in your training set.
            data_len = the number of timesteps (or features) in a single time-series signal.

          (n): number that dictates how many Gaussian neurons you are assigning to each timestep of your sequence

          (tmax): maximum spike time limit. Defines temporal coding window (spikes occur between 0 and tmax - 1)

          (beta, optional): hyperparameter used to scale the overlap (width) of your Gaussian curves

        """

        self.encoder = 'gaussian'
        self.tmax = tmax
        self.n = n
        self.beta = beta
        data_len = data.shape[1]

        # Save scaling parameters
        self.scaler = StandardScaler()
        data = torch.from_numpy(self.scaler.fit_transform(data))


        min_vals, _ = data.min(dim=0)
        max_vals, _ = data.max(dim=0)
        self.widths = beta*torch.abs(max_vals - min_vals) / (n - 2)
        self.widths = self.widths.unsqueeze(-1).repeat(1, self.n)  # --> shape = (data_len, n)

        self.centers = torch.zeros(data_len, n)
        for i in range(data_len):

            idx_vec = torch.arange(n)
            self.centers[i] = (
                min_vals[i] + ((2 * idx_vec - 3)/ 2) * self.widths[i]
            )


    def encode(self, tensor):

        """ Maps data tensor to spike times. Note: They do not need to be pre-scaled before calling

            Explanation: for every single point in the data_len-sized sample, encoder will generate a micro-schedule of spikes (tmax)
                            for 'n' encoding neurons to represent that specific value

        Args:
            (tensor): The raw time series signal of shape (data_len,) where data_len is the
                        number of timesteps in one signal => 1D Tensor representing 1 time-series example

        Returns:
            (tensor): The spike times where float('Inf') indicates not spike.
              The result has shape (data_len, n)

        """

        tensor = self.scaler.transform(tensor.unsqueeze(0)).squeeze()
        tensor = torch.from_numpy(tensor)

        tensor = tensor.unsqueeze(-1).repeat(1, self.n)
        exc = torch.exp(
            -0.5
            * ((tensor - self.centers) / self.widths)
            * ((tensor - self.centers) / self.widths)
        )
        spike_times = torch.floor((self.tmax + 1) * (1 - exc))
        spike_times[spike_times >= self.tmax] = float("Inf")

        return spike_times

"""### Delta Spike Encoder
This encoder captures signal **transitions** rather than absolute values.
It computes consecutive-sample differences on I and Q channels, then thresholds them into positive/negative spikes with magnitude bins.

**Best parameters (verified):** `threshold_percentile=50`, `n_mag_bins=3`, model `threshold=100`, **NO matched filter**.

Delta encoder achieves **99.7% accuracy / BER=0.0025** on low interference WITHOUT matched filter.
The matched filter destroys the delta information that this encoder relies on.
"""

class DeltaSpikeEncoder:
    """Encode I/Q windows by thresholding the difference between consecutive samples.

    For each channel (I and Q separately):
      - Compute delta[t] = x[t] - x[t-1]  (first delta is x[0] relative to 0)
      - If delta > +threshold -> positive spike neuron fires (1)
      - If delta < -threshold -> negative spike neuron fires (1)
      - Magnitude is encoded in extra magnitude bins

    Input:  interleaved I/Q window (I0,Q0,I1,Q1,...) shape (2*L,) where L=window_len
    Output: binary vector of length 2 * L * (2 + n_mag_bins)
            e.g. for L=63, n_mag_bins=3: 2 * 63 * 5 = 630

    IMPORTANT: Do NOT use matched filter with this encoder (use_mf=False).
    MF smooths out the transitions that delta encoding relies on.
    """

    def __init__(self, data, n_mag_bins=3, threshold_percentile=50):
        self.encoder = "delta"
        self.n_mag_bins = n_mag_bins

        # Fit threshold from training data
        data_np = data if isinstance(data, np.ndarray) else np.asarray(data)
        I = data_np[:, 0::2]
        Q = data_np[:, 1::2]
        deltas_I = np.diff(I, axis=1)
        deltas_Q = np.diff(Q, axis=1)
        all_abs_deltas = np.abs(np.concatenate([deltas_I.ravel(), deltas_Q.ravel()]))

        # Threshold at given percentile -- below this, no spike
        self.threshold = np.percentile(all_abs_deltas, threshold_percentile)
        if self.threshold < 1e-10:
            self.threshold = np.median(all_abs_deltas) + 1e-10

        # Magnitude bin edges for supra-threshold deltas
        supra = all_abs_deltas[all_abs_deltas > self.threshold]
        if len(supra) < 10:
            supra = all_abs_deltas
        pcts = np.linspace(0, 100, n_mag_bins + 1)
        self.mag_edges = np.percentile(supra, pcts)
        self.mag_edges[0] = self.threshold
        self.mag_edges[-1] = np.inf

    def encode(self, tensor):
        """Encode a single window (1D tensor of length 2*L) to binary vector."""
        x = tensor.numpy() if isinstance(tensor, torch.Tensor) else np.asarray(tensor)
        I = x[0::2]
        Q = x[1::2]
        L = len(I)

        channels = [I, Q]
        n_feat = 2 + self.n_mag_bins  # [pos_spike, neg_spike, mag_bin_0, ..., mag_bin_k]
        out_len = 2 * L * n_feat  # 2 channels
        out = np.zeros(out_len, dtype=np.float32)

        for ch_idx, ch in enumerate(channels):
            # Compute deltas with first element relative to zero
            deltas = np.empty(L, dtype=np.float32)
            deltas[0] = ch[0]
            deltas[1:] = np.diff(ch)

            abs_deltas = np.abs(deltas)
            offset = ch_idx * L * n_feat

            for t in range(L):
                base = offset + t * n_feat
                if abs_deltas[t] > self.threshold:
                    if deltas[t] > 0:
                        out[base] = 1.0       # positive spike
                    else:
                        out[base + 1] = 1.0   # negative spike

                    # Magnitude bin
                    mag_bin = np.searchsorted(self.mag_edges, abs_deltas[t]) - 1
                    mag_bin = min(max(mag_bin, 0), self.n_mag_bins - 1)
                    out[base + 2 + mag_bin] = 1.0

        return torch.from_numpy(out)

    @property
    def num_inputs_per_feature(self):
        return 2 + self.n_mag_bins


def encode_dataset_binary_delta(X, coder):
    """Encode dataset X (numpy, shape N x D) with a non-Gaussian encoder (delta, phase, etc.).
    Returns (N, num_inputs) float tensor of binary spike vectors.
    """
    results = []
    for i in range(X.shape[0]):
        x_t = torch.from_numpy(X[i].astype(np.float32)) if not isinstance(X[i], torch.Tensor) else X[i]
        results.append(coder.encode(x_t))
    return torch.stack(results, dim=0)

